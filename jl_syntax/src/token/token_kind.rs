/// 字句の種類
#[derive(Clone, Copy, Debug, PartialEq, Eq, PartialOrd, Ord)]
pub(crate) enum TokenKind {
    Eof,
    Space,
    Comment,
    Other,
    Int,
    Float,
    Char,
    Str,
    Ident,
    As,
    Break,
    Const,
    Continue,
    Else,
    Enum,
    Extern,
    False,
    Fn,
    For,
    Generic,
    If,
    In,
    Let,
    Loop,
    Match,
    Mod,
    Move,
    Mut,
    Pub,
    Ref,
    Return,
    Static,
    Struct,
    Super,
    True,
    Type,
    Use,
    While,
    LeftAngle,
    LeftAngleEqual,
    RightAngle,
    RightAngleEqual,
    LeftBrace,
    RightBrace,
    LeftBracket,
    RightBracket,
    LeftParen,
    RightParen,
    LeftSlimArrow,
    RightSlimArrow,
    RightFatArrow,
    LeftShift,
    LeftShiftEqual,
    RightShift,
    RightShiftEqual,
    And,
    AndAnd,
    AndAndEqual,
    AndEqual,
    Bang,
    BangEqual,
    Colon,
    Comma,
    Dot,
    Equal,
    EqualEqual,
    Hat,
    HatEqual,
    Minus,
    MinusEqual,
    Percent,
    PercentEqual,
    Pipe,
    PipeEqual,
    PipePipe,
    PipePipeEqual,
    PipeRight,
    Plus,
    PlusEqual,
    Semi,
    Slash,
    SlashEqual,
    Star,
    StarEqual,
}

impl Default for TokenKind {
    fn default() -> Self {
        TokenKind::Other
    }
}
